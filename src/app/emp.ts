export class Emp {
        id: any;
        name: string;
        department: any;
        joining_date: any;
        salary: any
    constructor(id: any, name: string, department: any, joining_date: any, salary: any) {
        this.id = id;
        this.name = name
        this.department = department;
        this.joining_date = joining_date;
        this.salary = salary
    }
}